export function applyWorkshopAr(cards: WorkshopCard[], locale: Locale): WorkshopCard[] {
  if (locale !== 'ar') return cards;
  return cards.map((c) => {
    const ar = workshopArOverrides[c.id];
    if (!ar) return c;
    return {
      ...c,
      title: ar.title,
      instructor: ar.instructor ?? c.instructor,
      details: ar.details,
    };
  });
}
